#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*dst2;
	const char	*src2 = src;

	dst2 = dst;
	while (dst && src && n--)
		*dst2++ = *src2++;
	return (dst);
}
