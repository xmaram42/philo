#include "libft.h"

static int	lookup(char c, char const *set)
{
	int	i;

	i = -1;
	while (set[++i])
		if (set[i] == c)
			return (1);
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	int	start;
	int	end;

	if (!s1 || !set)
		return (0);
	start = 0;
	end = ft_strlen(s1) - 1;
	while (lookup(s1[start], set))
		start++;
	while (lookup(s1[end], set))
		end--;
	return (ft_substr(s1, start, end - start + 1));
}
